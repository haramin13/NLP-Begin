import unicodedata
import regex as re
import emoji

# Xoa HTML code
def remove_html(txt):
    return re.sub(r'<[^>]*>', '', txt)

# Xoa URL
def remove_url(txt):
    return re.sub(r'https\S+', '', txt, flags=re.MULTILINE)

# Xoa emoji
def remove_emoji(txt):
    return emoji.replace_emoji(txt, replace='')

